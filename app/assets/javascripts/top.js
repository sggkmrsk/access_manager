$(function(){
  $("#door").hover(function(){
    $("#close").css('display','none')
    $("#open").css('display','block')
  },function(){
    $("#open").css('display','none')
    $("#close").css('display','block')
  })
  $("#cog").hover(function(){
    $("#spin").addClass('pulse-right')
  },function(){
    $("#spin").removeClass('pulse-right')
  })
})